#如果不希望操作直接作用于列表对象本身，那么可以使用列表的copy()方法创建一个完全一样的副本。
# 这样列表对象本身就不会发生变化。除了copy()方法外，使用切片操作和list函数也能达到同样的效果，如下。

a = [10, 20, 30, 40, 50]
b = a.copy()  # 使用copy()方法创建副本
c = a[:]  # 使用切片操作创建副本
d = list(a)  # 使用list函数创建副本
print(id(a), id(b), id(c), id(d))  # 查看各变量对象的id
b[2] = 'three'  # 修改副本第3个元素
print(b)
print(a)  # 原列表并没有发生变化
print(c)
print(d)

#4．查询列表元素
# 利用列表方法index()可查询指定元素在列表中第1次出现的位置索引。
#若列表不包含指定元素，则会出现错误提示，可以使用Python中的in关键字判断是否存在指定元素，
# 具体格式为“元素in列表对象”。若元素至少在列表中出现过一次，则返回True，否则返回False。
# index()方法和in关键字的应用示例如下。
letter = ['A', 'B', 'A', 'C', 'B', 'B', 'C', 'A']
print(letter.index('C'))  # 获取元素'C'在列表中第1次出现的位置索引
# 使用in关键字判断列表是否包含元素'A'
print('A' in letter)

