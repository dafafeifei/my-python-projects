#使用extend()方法能够将另一个列表增添到指定列表末尾，相当于将两个列表拼接。类似于字符串拼接，
# 两个列表对象也可以通过加号进行拼接，使用extend()方法的效果与使用自增运算（+=）的相同，如下。

# 创建一个列表对象month的副本，用于对比extend()方法与自增运算的效果
month=['January', 'February', 'March', 'April', 'May', 'June', 'July']
month_copy = month.copy()
print(month_copy)
others = ['August', 'September', 'November', 'December']
month.extend(others)  # 使用extend()方法将两个列表进行拼接
print(month)
month_copy += others  # 对副本进行自增运算
print(month_copy)

#类似于append()方法，使用insert()方法也能够向列表中增添一个元素。
# 不同的是，insert()方法可以指定增添位置，类似于在列表某个位置插入一个元素。
#只要向insert()方法中传入插入位置和要插入的元素，即可在列表的相应位置增添指定的元素。
# 若插入位置超出列表尾端，则元素会被置于列表最后。使用insert()方法插入元素示例如下通过加号进行拼接，
# 使用extend()方法的效果与使用自增运算（+=）的相同，如下。

month.insert(9, 'October')  # 在列表第10个位置上插入元素
print(month)
month.insert(20, 'None')  # 插入位置超出列表尾端
print(month)

#删除列表元素
#（1）del语句在Python中，使用del语句可以将列表元素删除。实质上，del语句是赋值语句（＝）的逆过程。
# 如果将赋值语句看作“向对象贴变量名标签”，那么del语句就是“将对象上的标签撕下来”，即将一个对象与它的变量名分离。
# 使用del语句可以将从列表中提取出的元素删除，如下。

month_copy = month.copy()  # 创建一个列表对象month的副本
del month_copy[-1]  # 删除副本中的最后一个元素
print(month_copy)

#（2）pop()方法利用元素位置可以对元素进行删除操作。将元素索引传入pop()方法中，
# 将会获取对应元素，并将其在列表中删除。若不指定元素位置，pop()方法将默认使用索引-1。
# 使用pop()方法删除元素的示例如下。

month_copy = month.copy()  # 创建一个列表对象month的副本
print(month_copy.pop(3))  # 获取并删除第4个元素
del_element = month_copy.pop()  # 将最后一个元素赋值给一个变量并在副本中将其删除
print(del_element)
print(month_copy) # 查看副本

#（3）remove()方法除了利用元素位置进行元素删除外，还可以对指定元素进行删除。
# 将指定元素传入remove()方法，则列表中第一次出现的该元素将会被删除，如下。

month.remove('None')  # 删除列表中的元素'None'
print(month)

