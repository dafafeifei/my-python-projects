#创建字典并进行增、删、改、查等操作
#1解析字典的键与值
#字典中的每个元素都具有与之对应且唯一的键，元素就是键所对应的值，键与值共同构成一个映射关系，
# 即键→值，每个键都可以映射到相应的值。
#键和值的这种映射关系在Python中具体表示为键:值（key:value），键和值之间用冒号隔开，
# 这里将其称为“键值对”，字典中会包含多组键值对。
#字典中的键必须使用不可变数据类型的对象，例如数字、字符串、元组等，并且键是不允许重复的；
# 而值则可以是任意类型的，且在字典中可以重复。

#2创建字典
#(1)使用花括号创建字典  将字典中的一系列键和值按键值对的格式传入花括号中，
# 并用逗号将各键值对隔开，即可实现字典的创建，具体格式如下。
#dict = {key_1:value_1, key_2:value_2, …, key_n:value_n}
#如果在花括号中不传入任何键值对，那么将会创建一个空字典。
#当在花括号中重复传入相同的键时，因为键在字典中不允许重复，
# 所以字典最终会采用最后出现的重复键的键值对。具体应用示例如下。

mydict1 = {'myint': 1, 'myfloat': 3.1415, 'mystr': 'name',
           'myint': 100, 'mytuple': (1, 2, 3), 'mydict': {}}  # 使用花括号创建字典
# 对于重复键，采用最后出现的对应键值对
print(mydict1)
empty_dict = {}  # 创建空字典
print(empty_dict)

#(2)使用dict函数创建字典
#     可以使用dict函数创建字典。Python中dict函数的作用主要是将包含双值子序列的序列对象转换为字典类型，
# 其中各双值子序列中的第1个元素作为字典的键，第2个元素作为键对应的值，即双值子序列中包含键值对信息。
#     还可以直接向dict函数传入键和值创建字典，其中，键和值应通过“=”隔开。这种创建方式不允许键重复，
# 否则会返回错误。具体格式如下。dict = {key_1:value_1, key_2:value_2, …, key_n:value_n}

# 使用dict函数转换列表对象为字典对象
mydict1 = dict([('myint', 1), ('myfloat', 3.1415), ('mystr', 'name'),
                ('myint', 100), ('mytuple', (1, 2, 3)), ('mydict', {})])
print(mydict1)
mydict2 = dict(zero=0, one=1, two=2)  # 使用dict函数创建字典
print(mydict2)
empty_dict = dict()  # 创建空字典
print(empty_dict)

