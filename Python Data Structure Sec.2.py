#4 字典常用方法和函数  字典常用方法和函数
#在Python的内置数据结构当中，列表和字典是最为灵活的数据结构。类似于列表，字典也属于可变数据结构，
# 因此字典也含有丰富且功能强大的方法和函数。
#与列表一样，字典中也有copy()方法，其作用是复制字典内容并创建一个副本对象。
# 由于上述字典处理会直接作用在字典对象上，而且各种处理方式包含多种方法。

#[1]增添字典元素

#（1）使用键访问赋值增添元素
#利用字典元素提取方法传入一个新的键，并对这个新键进行赋值操作，即dict [newkey] = new_value，
# 字典中就会产生新的键值对。这种赋值操作可能会因为键不存在而出现错误，示例代码如下。
country = dict(China='Beijing',
               England='London',
               France='Paris',
               Canada='Ottawa')  # 使用dict函数创建字典
country_copy = country.copy()  # 创建一个字典对象副本
country_copy['Russia'] = 'Moscow'  # 增添元素
print(country_copy)

#(2)使用update()方法合并字典
#字典方法update()能将两个字典进行合并，传入字典中的键值对会被复制并增添到调用此方法的字典对象中。
# 如果两 个 字典中存在相同的键，那么传入字典中的键所对应的值会替换掉调用update()方法的字典对象中的原有值，
# 从而实现值更新的效果，示例代码如下。

others = dict(Australia='Canberra', Japan='tokyo', Canada='OTTAWA')
country.update(others)  # 使用update()方法增添多个元素
print(country)

#[2]删除字典元素

#（1）使用del语句删除字典元素
# 使用del语句删除字典元素的具体格式为del dict [key]，应用示例如下。
country_copy = country.copy()
del country_copy['Canada']  # 使用del语句删除副本对象中的元素
print(country_copy)

#（2）使用pop()方法删除字典元素
# 向pop()方法传入需要删除的元素的键，将会返回对应的值，并在字典当中删除相应的键值对。
# 若将返回的结果赋值给变量，则相当于从字典当中抽离出值，示例如下。

old_value = country.pop('Canada')  # 将键对应的值赋值给变量，并删除键值对
print(old_value)
print(country)  #值给old_value了

#（3）使用clear()方法删除字典元素
# clear()方法可以删除字典中的所有元素，最终返回一个空字典，代码如下。

country_copy = country.copy()
country_copy.clear()  # 清空副本对象内容
print(country_copy)

#[3]修改字典元素
# 要修改字典中的某个元素，同样可以使用键访问赋值实现，其格式为dict[key] = new_value。
# 赋值操作在字典中的使用非常灵活，无论键是否存在于字典中，所赋予的新值都会覆盖或增添到字典中。具体应用示例如下。

country['Japan'] = 'Tokyo'  # 直接将新值赋值给对应元素
print(country)

#[4]查询和获取字典元素信息
#在实际应用当中，往往需要查询某个键或值是否存在于字典当中，除了可以使用提取字典元素的方法进行查询外，
# 还可以使用Python中的in关键字进行查询。字典的方法中有3种方法可以用于获取键值信息。
#（1）keys()：用于获取字典中的所有键。
#（2）values()：用于获取字典中的所有值。
#（3）items()：用于获取字典中的所有键值对。
#调用以上3种方法返回的结果分别是字典中键、值和键值对的迭代形式，可以通过list函数将返回结果转换为列表类型，
# 同时可以配合使用in关键字，判断键值和键值对是否存在于字典中。具体应用示例如下。

#获取键值信息
# 判断键是否存在于字典中
print('Canada' in country)
# 获取所有键
all_keys = country.keys()  # 使用keys()方法获取所有键
print(all_keys)
all_values = country.values()  # 使用values()方法获取所有值
print(all_values)

print('Beijing' in all_values)  # 判断值是否存在于字典中

print(list(all_values))  # 将值的迭代形式转换为列表

all_items = country.items()  # 使用items()方法获取所有键值对

print(all_items)

print(('Australia', 'Canberra') in all_items)  # 判断键值对是否存在于字典中

print(list(all_items))  # 将键值对的迭代形式转换为列表

